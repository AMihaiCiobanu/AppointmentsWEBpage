// ─── Language detection ───────────────────────────────────────────────────────

function detectLang() {
  // Path-based routing takes priority: /ro/, /es/, /it/ etc.
  const pathLang = window.location.pathname.match(/^\/([a-z]{2})\//);
  if (pathLang && TRANSLATIONS[pathLang[1]]) return pathLang[1];
  const urlParam = new URLSearchParams(window.location.search).get('lang');
  if (urlParam && TRANSLATIONS[urlParam]) return urlParam;
  try {
    const stored = localStorage.getItem('lang');
    if (stored && TRANSLATIONS[stored]) return stored;
  } catch (_) { }
  const nav = (navigator.language || '').slice(0, 2).toLowerCase();
  if (TRANSLATIONS[nav]) return nav;
  return 'en';
}

// ─── Apply language ───────────────────────────────────────────────────────────

function applyLang(lang) {
  if (!TRANSLATIONS[lang]) lang = 'en';
  const t = TRANSLATIONS[lang];
  const langShort = {
    en: 'EN',
    ro: 'RO',
    es: 'ES',
    fr: 'FR',
    it: 'IT',
    pt: 'PT',
    bg: 'BG',
    pl: 'PL',
  };

  document.documentElement.lang = lang;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.textContent = t[key];
  });

  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (t[key] !== undefined) el.placeholder = t[key];
  });

  document.querySelectorAll('[data-i18n-alt]').forEach(el => {
    const key = el.dataset.i18nAlt;
    if (t[key] !== undefined) el.setAttribute('alt', t[key]);
  });

  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
    const key = el.dataset.i18nAriaLabel;
    if (t[key] !== undefined) el.setAttribute('aria-label', t[key]);
  });

  if (t.page_title) document.title = t.page_title;
  const bookingTitleEl = document.getElementById('booking-doc-title');
  if (bookingTitleEl && t.booking_doc_title !== undefined) {
    bookingTitleEl.textContent = t.booking_doc_title;
  }
  const metaDesc = document.querySelector('meta[name="description"]');
  if (metaDesc && t.meta_description) metaDesc.setAttribute('content', t.meta_description);

  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.dataset.lang === lang);
  });

  document.querySelectorAll('#lang-btn-label').forEach(el => {
    el.textContent = langShort[lang] || 'EN';
  });

  try { localStorage.setItem('lang', lang); } catch (_) { }
}

// ─── Expose on window so module scripts (booking/app.js) can access ──────────

window.TRANSLATIONS = TRANSLATIONS;
window.applyLang = applyLang;
window.detectLang = detectLang;

// ─── Apply language immediately (script runs after DOM is parsed) ─────────────

applyLang(detectLang());

// ─── Wire up selector on DOM ready ───────────────────────────────────────────

document.addEventListener('DOMContentLoaded', () => {
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.addEventListener('click', () => {
      applyLang(opt.dataset.lang);
      // Language selector UI may not exist on all pages.
      const langDropdown = document.querySelector('.lang-dropdown');
      const langBtn = document.querySelector('.lang-toggle');
      langDropdown?.classList.remove('open');
      langBtn?.classList.remove('open');
    });
  });
});
